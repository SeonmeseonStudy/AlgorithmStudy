import java.io.*
fun main(){
    val br = BufferedReader(InputStreamReader(System.`in`))
    val n = br.readLine().split(" ").map{it.toInt()}
    val arr = Array(n[0]){""}
    for(i in 0 until n[0]){
        arr[i] = br.readLine()
    }
    var count =0
    for(i in 0 until n[1]){
        if(arr.contains(br.readLine())){
            count++
        }
    }
    println(count)
}
